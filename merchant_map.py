def classify_merchant(merchant: str) -> str:
    """
    مطابقة احتواء مو تامة — عشان "محطة بترومين" تطابق "بترومين".
    نرتب المفاتيح بالطول تنازليًا: "لولو هايبر" قبل "لولو".
    """
    m = (merchant or "").strip().lower()
    if not m:
        return "غير محدد"
    for key in sorted(MERCHANT_MAP, key=len, reverse=True):
        if key.lower() in m:
            return MERCHANT_MAP[key]
    return "غير محدد"


